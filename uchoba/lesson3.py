x = 15
y = 20
print("x = ", x)
print("y = ", y)

#Основные математический операции
print("x + y = ", x + y)
print("x - y = ", x - y)
print("x * y = ", x * y)
print("x / y = ", x / y)
print("x % y = ", x % y)
print("x // y = ", x // y)
print("x ** y = ", x ** y)

#Бытовые операции
a = 5
b = 7
print("x = ", a, "=", bin(a))
print("x = ", a, "=", hex(a))
print("x = ", a, "=", hex(20))
print("x = ", a, "=", oct(20))
print("----------")

print("x = ", a | b)



